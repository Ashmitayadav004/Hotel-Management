    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        try {
            // TODO add your handling code here:
            pst = con.prepareStatement("select* from login where email=? and ans=? ");

            pst.setString(1, txtemail.getText());
            pst.setString(2, txtans.getText());
            rs = pst.executeQuery();

            if (rs.next()) {

                try {
                    // TODO add your handling code here:
                    pst = con.prepareStatement("update login set password=? where email=? and ans=? ");
                    pst.setString(1, txtpassword.getText());
                    pst.setString(2, txtemail.getText());
                    pst.setString(3, txtans.getText());
                     pst.executeUpdate();
                    JOptionPane.showMessageDialog(this, " Reset Successfull \n Login now");

                } catch (SQLException ex) {
                    Logger.getLogger(forgot.class.getName()).log(Level.SEVERE, null, ex);
                }
            } else {
                JOptionPane.showMessageDialog(this, " Answer not match");
            }

        } catch (SQLException ex) {
            Logger.getLogger(forgot.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_jButton6ActionPerformed

