    private void jButton9ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton9ActionPerformed
        try {
            // TODO add your handling code here:
            pst = con.prepareStatement("select sq from login where email=?");
            pst.setString(1, txtemail.getText());
            rs = pst.executeQuery();

            if (rs.next()) {
                sq.setText(rs.getString("sq"));
            } else {
                JOptionPane.showMessageDialog(this, " Sorry invalid email ");
            }
        } catch (SQLException ex) {
            Logger.getLogger(forgot.class.getName()).log(Level.SEVERE, null, ex);
        } finally {
            // Close the PreparedStatement
            if (pst != null) {
                try {
                    pst.close();
                } catch (SQLException e) {
                    Logger.getLogger(forgot.class.getName()).log(Level.SEVERE, null, e);
                }
            }
        }


    }//GEN-LAST:event_jButton9ActionPerformed

