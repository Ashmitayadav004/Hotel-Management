    public void ShowRecordInTable() {
        try {
            pst = con.prepareStatement("SELECT * FROM room"); // Corrected SQL query
            rs = pst.executeQuery();
            DefaultTableModel tmodel = (DefaultTableModel) jTable.getModel();
            tmodel.setRowCount(0); // Clear existing rows

            if (!rs.next()) {
                System.out.println("No data found in the room table."); // Debugging line
                return; // Exit if no data
            }

            do {
                Vector<String> column = new Vector<>();
                column.add(rs.getString("roomnumber"));
                column.add(rs.getString("roomtype"));
                column.add(rs.getString("bed"));
                column.add(rs.getString("price"));
                column.add(rs.getString("status"));
                tmodel.addRow(column);
            } while (rs.next());

        } catch (SQLException ex) {
            Logger.getLogger(ManageRoom.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

