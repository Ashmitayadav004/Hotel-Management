    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if (txtroomno.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "All field should be filled");
            txtroomno.requestFocus();
        } else if (txtprice.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "All field should be filled");
            txtprice.requestFocus();
        } else {
            try {
                pst = con.prepareStatement("SELECT * FROM room where roomnumber=?");
                pst.setString(1, txtroomno.getText());
                rs = pst.executeQuery();
                if (rs.next()) {
                    JOptionPane.showMessageDialog(this, "Already Allocated this Room number");
                } else {
                    pst = con.prepareStatement("INSERT INTO room (roomnumber, roomtype, bed, price, status) VALUES (?,?,?,?,?)");
                    pst.setString(1, txtroomno.getText());
                    pst.setString(2, jComboBoxroomtype.getItemAt(jComboBoxroomtype.getSelectedIndex()));
                    pst.setString(3, jComboBoxbed.getItemAt(jComboBoxbed.getSelectedIndex()));
                    pst.setString(4, txtprice.getText());
                    pst.setString(5, "Not Booked");
                    pst.executeUpdate();
                    JOptionPane.showMessageDialog(this, "Room Added");
                    ShowRecordInTable();
                    txtroomno.setText("");
                    txtprice.setText("");

                }

            } catch (SQLException ex) {
                Logger.getLogger(ManageRoom.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

    }//GEN-LAST:event_jButton2ActionPerformed

